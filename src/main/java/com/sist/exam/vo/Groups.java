package com.sist.exam.vo;

public class Groups {

	private String group_no;
	private String group_name;
	private String group_begin;
	private String group_due;
	
	
	public String getGroup_no() {
		return group_no;
	}
	public void setGroup_no(String group_no) {
		this.group_no = group_no;
	}
	public String getGroup_name() {
		return group_name;
	}
	public void setGroup_name(String group_name) {
		this.group_name = group_name;
	}
	public String getGroup_begin() {
		return group_begin;
	}
	public void setGroup_begin(String group_begin) {
		this.group_begin = group_begin;
	}
	public String getGroup_due() {
		return group_due;
	}
	public void setGroup_due(String group_due) {
		this.group_due = group_due;
	}
	
	
	
	
	
	
	
}

